
p = 0xFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFEFFFFFC2F

def is_valid_sepc265k1_point(n):
    x, y = n
    x = int(x)
    y = int(y)

    # y^2 = x^3 + 7
    left = (y**2) % p
    right = ((x**3) + 7) % p
    return left == right

point = (1, 2)

G = (
    0x79BE667EF9DCBBAC55A06295CE870B07029BFCDB2DCE28D959F2815B16F81798,
    0x483ADA7726A3C4655DA4FBFC0E1108A8FD17B448A68554199C47D08FFB10D4B8,
)

G2 = (
    89565891926547004231252920425935692360644145829622209833684329913297188986597,
    12158399299693830322967808612713398636155367887041628176798871954788371653930,
)

G10_inverse = (
    72488970228380509287422715226575535698893157273063074627791787432852706183111,
    -62070622898698443831883535403436258712770888294397026493185421712108624767191,
)

assert(not is_valid_sepc265k1_point(point))
assert(is_valid_sepc265k1_point(G))
assert(is_valid_sepc265k1_point(G2))
assert(is_valid_sepc265k1_point(G10_inverse))
